package main
import "fmt"

func main() { 
    myVar1 := 39
    myVar2 := "Cogito Ergo Sum"
    myVar3 := 232.03
    
    fmt.Printf("myVar1's value: %d, ", myVar1)
    fmt.Printf("myVar1's type : %T\n", myVar1)
    fmt.Printf("myVar2's value: %s, ", myVar2)
    fmt.Printf("myVar2's type : %T\n", myVar2)
    fmt.Printf("myVar3's value: %f, ", myVar3)
    fmt.Printf("myVar3's type : %T\n", myVar3)
}
